public class Cliente {
    
    private String nome;
    private int cpf;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the CPF
     */
    public int getCpf() {
        return cpf;
    }

    /**
     * @param CPF the CPF to set
     */
    public void setCpf(int CPF) {
        this.cpf = CPF;
    }

  
    }
    
    

