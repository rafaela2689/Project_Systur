    //seleciona uma linha na tabela para alterar ou excluir dados 
    private void linhaSelecionada(JTable tabela) {
        if (jTabPesquisa.getSelectedRow() != -1) {
            habilitaDados();
            jtfIdServico.setText(String.valueOf(servico.get(tabela.getSelectedRow()).getIdServico()));
            jtfValor.setText(String.valueOf(servico.get(tabela.getSelectedRow()).getValor()));
            jtfNome.setText(servico.get(tabela.getSelectedRow()).getNome());
            jtfDescricao.setText(servico.get(tabela.getSelectedRow()).getDescricao());
            jtfLotMax.setText(String.valueOf(servico.get(tabela.getSelectedRow()).getLotMax()));
            jtfLotMin.setText(String.valueOf(servico.get(tabela.getSelectedRow()).getLotMin()));
            jtfObs.setText(servico.get(tabela.getSelectedRow()).getObs());
        } else {

            jtfIdServico.setText("");
            jtfValor.setText("");
            jtfNome.setText("");
            jtfDescricao.setText("");
            jtfLotMax.setText("");
            jtfLotMin.setText("");
            jtfObs.setText("");
        }
    }

    //verifica se foi deixado algum campo obrigatório em branco
    public boolean verificaDados() {
        if ((!jtfValor.getText().equals("")) && (!jtfNome.getText().equals(""))
                && (!jtfDescricao.getText().equals("")) && (!jtfLotMax.getText().equals(""))
                && (!jtfLotMin.getText().equals(""))) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Campos obrigatórios em branco!");
            return false;
        }
    }

    //mostra a pesquisa na tabela
    private void mostraPesquisa(List<ServicoTuristico> servico) {

        while (tmServico.getRowCount() > 0) {
            tmServico.removeRow(0);
        }
        if (servico.size() == 0) {
            JOptionPane.showMessageDialog(null, "Nenhum veículo cadastrado!");
        } else {
            String[] linha = new String[]{null, null, null, null, null, null, null};
            for (int i = 0; i < servico.size(); i++) {

                tmServico.addRow(linha);
                tmServico.setValueAt(servico.get(i).getIdServico(), i, 0);
                tmServico.setValueAt(servico.get(i).getValor(), i, 1);
                tmServico.setValueAt(servico.get(i).getNome(), i, 2);
                tmServico.setValueAt(servico.get(i).getDescricao(), i, 3);
                tmServico.setValueAt(servico.get(i).getLotMax(), i, 4);
                tmServico.setValueAt(servico.get(i).getLotMin(), i, 5);
                tmServico.setValueAt(servico.get(i).getObs(), i, 6);

            }
        }
    }

    //altera os dados selecionados na tabela
    private void alterar() throws SQLException {
        if (jTabPesquisa.getSelectedRow() != -1) {
            if (verificaDados()) {
                ServicoTuristico v1 = new ServicoTuristico();
                ServicoDao dao = new ServicoDao();
                v1.setIdServico(Integer.valueOf(jtfIdServico.getText()));
                v1.setValor(Double.valueOf(jtfValor.getText()));
                v1.setNome(jtfNome.getText());
                v1.setDescricao(jtfDescricao.getText());
                v1.setLotMax(Integer.parseInt(jtfLotMax.getText()));
                v1.setObs(jtfObs.getText());
                dao.altera(v1);
                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
            }
        }
    }
