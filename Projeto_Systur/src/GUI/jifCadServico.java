    public void listarServicos() throws SQLException {
        ServicoDao dao = new ServicoDao();
        servico = dao.getLista("%" + jtfPesquisaServico.getText() + "%");
        mostraPesquisa(servico);
    }

    //cadastra novo veiculo
    public void cadastro() {
        try {
            ServicoTuristico v = new ServicoTuristico();

            v.setValor(Double.parseDouble(jtfValor.getText()));
            v.setNome(jtfNome.getText());
            v.setDescricao(jtfDescricao.getText());
            v.setLotMax(Integer.parseInt(jtfLotMax.getText()));
            v.setLotMin(Integer.parseInt(jtfLotMin.getText()));
            v.setObs(jtfObs.getText());

            ServicoDao vd = new ServicoDao();
            vd.adiciona(v);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            //JOptionPane.showMessageDialog(null, "Erro no botão cadastrar\n" + ex);
        }
    }

    public void excluir() throws SQLException {
        int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir os dados? ",
                "Confirmação", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_NO_OPTION) {
            ServicoDao dao = new ServicoDao();
            dao.exclui(servico.get(jTabPesquisa.getSelectedRow()));
            mostraPesquisa(servico);
        }
    }
